// Helper functions
export const getEntertainmentApps = () => {
    return Object.entries(entertainmentApps).map(([bundleId, info]) => ({
        bundleId,
        ...info,
        enabled: true
    }));
};

export const getAppsByCategory = (category) => {
    return Object.entries(entertainmentApps)
        .filter(([_, info]) => info.category === category)
        .map(([bundleId, info]) => ({
            bundleId,
            ...info,
            enabled: true
        }));
};

export const getCategories = () => {
    const categories = new Set();
    Object.values(entertainmentApps).forEach(app => {
        categories.add(app.category);
    });
    return Array.from(categories);
};

